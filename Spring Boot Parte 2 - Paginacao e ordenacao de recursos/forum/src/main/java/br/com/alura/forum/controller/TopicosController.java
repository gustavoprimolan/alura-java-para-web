	//PARA OS CAMPOS COM A PAGINACAO SERAO PASSADOS VIA URL E O SPRING VAI PEGAR AUTOMATICO POR CONTA DA ANOTACAO NA CLASSE ForumApplication
	//OS CAMPOS NA URL SAO: page=0&size=10&sort=id,asc
